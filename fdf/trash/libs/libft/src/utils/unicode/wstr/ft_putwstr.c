#include <stddef.h>
#include "libft/utils/unicode.h"

void			ft_putwstr(const wchar_t *s)
{
	char		unicode;

	if (s)
	{
		unicode = ft_getunicode();
		if (unicode == UNI_UTF8)
			ft_putwstr_utf8(s);
	}
}
